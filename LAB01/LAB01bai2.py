a = int(input("Nhập số nguyên a: "))
b = int(input("Nhập số nguyên b: "))
tong = a + b
print("Kết quả tổng: ", tong)
hieu = a - b 
print("Kết quả hiệu: ", hieu)
tich = a * b
print("Kết quả tích: ", tich)
hieu = round(a / b, 3)
print("Kết quả thương: ", hieu)